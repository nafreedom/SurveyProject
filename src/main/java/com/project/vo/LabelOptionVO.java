package com.project.vo;

public class LabelOptionVO {
    private int labelSeq;
    private int labelOptionNum;
    private String labelOptionValue;

    public int getLabelSeq() {
        return labelSeq;
    }

    public void setLabelSeq(int labelSeq) {
        this.labelSeq = labelSeq;
    }

    public int getLabelOptionNum() {
        return labelOptionNum;
    }

    public void setLabelOptionNum(int labelOptionNum) {
        this.labelOptionNum = labelOptionNum;
    }

    public String getLabelOptionValue() {
        return labelOptionValue;
    }

    public void setLabelOptionValue(String labelOptionValue) {
        this.labelOptionValue = labelOptionValue;
    }
}
