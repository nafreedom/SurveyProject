package com.project.vo;

public class SurveySettingVO {
    private int surveyNum;
    private int emailCollect;
    private int emailOpen;

    public int getSurveyNum() {
        return surveyNum;
    }

    public void setSurveyNum(int surveyNum) {
        this.surveyNum = surveyNum;
    }

    public int getEmailCollect() {
        return emailCollect;
    }

    public void setEmailCollect(int emailCollect) {
        this.emailCollect = emailCollect;
    }

    public int getEmailOpen() {
        return emailOpen;
    }

    public void setEmailOpen(int emailOpen) {
        this.emailOpen = emailOpen;
    }
}
