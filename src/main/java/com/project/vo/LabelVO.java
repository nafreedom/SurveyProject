package com.project.vo;

public class LabelVO {
    private int surveyNum;
    private int labelNum;
    private String labelName;
    private String labelNickname;

    public int getSurveyNum() {
        return surveyNum;
    }

    public void setSurveyNum(int surveyNum) {
        this.surveyNum = surveyNum;
    }

    public int getLabelNum() {
        return labelNum;
    }

    public void setLabelNum(int labelNum) {
        this.labelNum = labelNum;
    }

    public String getLabelName() {
        return labelName;
    }

    public void setLabelName(String labelName) {
        this.labelName = labelName;
    }

    public String getLabelNickname() {
        return labelNickname;
    }

    public void setLabelNickname(String labelNickname) {
        this.labelNickname = labelNickname;
    }
}
